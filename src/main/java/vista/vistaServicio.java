    private void bntProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntProcesarActionPerformed
        // TODO add your handling code here:

        int cod = Integer.parseInt(txtCod.getText());
        servicio ep = obj.consulta(cod);
        if (ep == null) {
            txtRes.setText("No existe codigo" + cod);
            return;

        }

        txtRes.setText("Tipo de servicio :" + ep.getTipoSer());
        txtRes.append("\n Precio :" + ep.getPrecio());

    }//GEN-LAST:event_bntProcesarActionPerformed

