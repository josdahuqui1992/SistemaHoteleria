    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        // TODO add your handling code here:
        servicio f = new servicio();
        f.setTipoSer(txtServicio.getText());
        f.setPrecio(Double.parseDouble(txtPrecio.getText()));
        f.total();
        obj.adiciona(f);
        JOptionPane.showMessageDialog(null, "Grabacion ok");
        txtRes2.setText("Nombre :" + txtServicio.getText());
        txtRes2.append("\n Precio :" + txtPrecio.getText());

        txtServicio.setText("");
        txtPrecio.setText("");
    }//GEN-LAST:event_btnProcesarActionPerformed

